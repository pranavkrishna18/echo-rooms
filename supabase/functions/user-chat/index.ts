import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers":
    "authorization, x-client-info, apikey, content-type, x-supabase-client-platform, x-supabase-client-platform-version, x-supabase-client-runtime, x-supabase-client-runtime-version",
};

serve(async (req) => {
  if (req.method === "OPTIONS") return new Response(null, { headers: corsHeaders });

  try {
    const { messages } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    if (!LOVABLE_API_KEY) throw new Error("LOVABLE_API_KEY is not configured");

    const systemPrompt = `You are EchoBot — a friendly AI assistant for EchoRoom, a mental health support community platform. You help regular users navigate the platform and answer their questions.

You can help with:
- How to use the platform (posting, replying, creating rooms, joining rooms)
- How to speak in a specific room (go to the room page and use the "Share Anonymously" text box)
- Privacy and anonymity features (all posts are anonymous with random aliases)
- Emotional support and resources
- Understanding room topics and finding the right room
- General mental health awareness (you are NOT a therapist — always recommend professional help for serious issues)

Key platform features to know:
- Users can browse rooms at /rooms and click any room to enter
- Inside a room, users type in the "Share Anonymously" box and click "Post" to share
- All posts are anonymous — users get random aliases
- AI automatically detects the emotion of each post
- EchoBot replies to every post with supportive messages
- Users can reply to others' posts to offer support
- Users can create new rooms with custom topics via "Create Room"
- The Mood Trends chart shows weekly emotional patterns in each room

Be warm, supportive, concise, and helpful. Use emoji sparingly. Format with markdown when useful.`;

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-3-flash-preview",
        messages: [
          { role: "system", content: systemPrompt },
          ...messages,
        ],
        stream: true,
      }),
    });

    if (!response.ok) {
      if (response.status === 429) {
        return new Response(JSON.stringify({ error: "Rate limit exceeded, please try again later." }), {
          status: 429, headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      if (response.status === 402) {
        return new Response(JSON.stringify({ error: "Payment required." }), {
          status: 402, headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      const t = await response.text();
      console.error("AI gateway error:", response.status, t);
      return new Response(JSON.stringify({ error: "AI gateway error" }), {
        status: 500, headers: { ...corsHeaders, "Content-Type": "application/json" },
      });
    }

    return new Response(response.body, {
      headers: { ...corsHeaders, "Content-Type": "text/event-stream" },
    });
  } catch (e) {
    console.error("user-chat error:", e);
    return new Response(JSON.stringify({ error: e instanceof Error ? e.message : "Unknown error" }), {
      status: 500, headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  }
});
